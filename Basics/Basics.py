from threading import Thread , active_count
from time import sleep
import time
starttime = time.perf_counter()
print(active_count())
# sleep(3)


def task1():
  print("starttime1")
  
  for i in range(10000000):
    count = 0
    count += 1
  print("endtime 1")
  

def task2():
  print("starttime2")
  
  for i in range(10000000):
    count = 0
    count += 1
  print("endtime 2")
  
  
def task3():
  print("starttime3")
  
  for i in range(10000000):
    count = 0
    count += 1
  print("endtime 3")
  
task1 = Thread(target=task1)
task2 = Thread(target=task2)
task3 = Thread(target=task3)



task1.start()
task2.start()
task3.start()

print(active_count())

task1.join()
task2.join()
task3.join()
print(f"Program finished in {time.perf_counter() - starttime} seconds")

